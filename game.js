async function connectWallet() {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: 'eth_requestAccounts' });
        contract = new web3.eth.Contract(contractABI, contractAddress);
        updateUI();
    } else {
        alert("Please install MetaMask!");
    }
}

async function createCharacter() {
    const accounts = await web3.eth.getAccounts();
    const name = document.getElementById("charName").value;
    await contract.methods.createCharacter(name).send({ from: accounts[0] });
    updateUI();
}

async function collectResources() {
    const accounts = await web3.eth.getAccounts();
    await contract.methods.collectResources().send({ from: accounts[0] });
    updateUI();
}

async function craftSword() {
    const accounts = await web3.eth.getAccounts();
    await contract.methods.craftSword().send({ from: accounts[0] });
    updateUI();
}

async function fightEnemy() {
    const accounts = await web3.eth.getAccounts();
    await contract.methods.fightEnemy().send({ from: accounts[0] });
    updateUI();
}

async function updateUI() {
    const accounts = await web3.eth.getAccounts();
    const char = await contract.methods.getCharacter(accounts[0]).call();
    const res = await contract.methods.getResources(accounts[0]).call();
    document.getElementById("charInfo").innerText = `${char[0]} (Attack: ${char[1]}, Health: ${char[2]})`;
    document.getElementById("resources").innerText = `Wood: ${res[0]}, Metal: ${res[1]}`;
}

